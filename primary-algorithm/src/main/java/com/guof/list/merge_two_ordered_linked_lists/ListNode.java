package com.guof.list.merge_two_ordered_linked_lists;

/**
 * ClassName: ListNode
 * Package: com.feng.delete_the_penultimate_node_of_the_linked_list
 * Description:
 *
 * @Author guof
 * @Create 2023/3/25 18:27
 * @Version 1.0
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}